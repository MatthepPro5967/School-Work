void GetUserGuess(char s[])
{   //input: char s
    //returned value: none
    //purpose: get user guess and correct it
    
    char guess[20];
   
    int counter = 0;
    char LOWER[] = "abcdefghijklmnopqrstuvwxyz";
    char UPPER[] = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
    int ggg = 0; //separate counter to keep track of upper and lowecase letters
   
   
   do
   {
        ggg = 0;
        
        for(int n = 0; s[n] != '\0'; ++n) //set counter = to number of letters of vocab word
            counter = n + 1;
        //printf("counter00000 = %i", counter);
        
        printf("Enter guess: ");
        scanf("%s", guess);
    
        
        do
        {
            for(int n = 0; UPPER[n] != '\0'; ++n)
            {
                if(UPPER[n] == guess[ggg])
                {
                    guess[ggg] = LOWER[n];
                    //printf("\n%c = %c", guess[ggg], LOWER[n]);
                    //printf("guess[ggg] = %c", guess[ggg]);
                }
            
    
            }
            ++ggg;
            //printf("\nggg = %i", ggg);
        }while(ggg <= counter);
        
        ggg = 0;
        
        for(int n = 0; guess[ggg] != '\0'; ++n)
        {
            if(guess[ggg] == s[n])
            {
                counter -= 1;
                ++ggg;
            }
            else
            {
                counter += 1;
                ++ggg;
            }
        }
        //printf("\ncounter222 = %i", counter);
   }while(counter != 0);
    
    
    
    
}
